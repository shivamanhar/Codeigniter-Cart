<h1>Köpvillkor</h1>


<dl>
    <dt>1. Köp</dt>
    <dd>Beställningar kan göras på hemsidan samt via e-post.
    Alla falska beställningar kommer att polisanmälas.</dd>
    <dt>2. Pris</dt>
    <dd>Priserna är angivna i svenska kronor. Alla priser är inklusive moms, för närvarande 25%.
    Vid förändring av pris från leverantören, förbehåller vi oss rätten att ändra dessa priser/villkor med
    omedelbar verkan. Priser som gäller vid beställningstillfället gäller även vid leveranstillfället.</dd>
    <dt>3. Betalning</dt>
    <dd>All betalning (just nu) sker mot postförskott, annan betalningslösning kan göras vid enskilda fall.</dd>
    <dt>4. Leverans</dt>
    <dd>Just nu är våran leveranstid tyvärr upp till 1-2 veckor. För alla outhämtade paket så kommer
    vi fakturera kunden på 350 kr för vårt arbete med hantering av varorna.</dd>
    <dt>5. Bytesrätt</dt>
    <dd>Bytesrätt gäller 10 dagar från köptillfället.<br />
        Följande villkor gäller;<br />
        1. Varan är helt obegagnad och felfri.<br />
        2. Komplett originalförpackning återlämnas i sin helhet och helt oskadd.<br />
        3. Kvitto eller faktura finns och kan uppvisas.<br />
        4. Ange anledning till bytet och bifoga kvitto eller faktura.<br />
        Om varan är felexpedierad står vi självklart för returfrakt annars står du för alla ev. fraktkostnader.<br />
        Om varan är felexpedierad skall du kontakta omedelbart oss för mer exakt information om hur du gör returen. 
    </dd>
    <dt>6. Reklamationer</dt>
    <dd>Vid fel leverans, såsom fel vara, fel antal varor, skadade varor så skall alltid beställaren senast 5 dagar efter att felet upptäckts kontakta Smartprodukt och uppge vari felet består. Returfrakten återbetalas så snart varan inkommit och reklamationen godkänts. Ersättning av ny vara sker kostnadsfritt för beställaren under förutsättning att den fortfarande är förpackad i obruten originalförpackning.</dd>
    <dt>7. Kundtjänst</dt>
    <dd>Vid synpunkter eller frågor kontakta oss via telefon eller e-post som du kan hitta <a href="<?php echo site_url(); ?>/contact">här</a>.</dd>
    <dt>8. Loggning och lagring av kunduppgifter</dt>
    <dd>Vi förmedlar aldrig dina personuppgifter vidare. Vi loggar och sparar
    alla användares IP-adresser och tidpunkt enbart för falskbeställningar eller annan olaglig aktivitet.</dd>
    <dt>9. Force Majeure</dt>
    <dd>Händelser såsom krig, naturkatastrof, stridsåtgärder på arbetsmarknaden, myndighetsbeslut, uteblivna leveranser från underleverantörer, fördyrande omständigheter samt likvärdiga händelser utanför vår kontroll, vilken ej kunnat förutses, skall hänföras till force majeure, vilket innebär att Smartprodukt befrias från sina förpliktelser att fullgöra ingångna avtal.</dd></dt>
    <dt>10. Övrigt</dt>
    <dd>Smartprodukt förbehåller sig rätten att ändra dessa köpvillkor med omedelbar verkan.</dd>
</dl>
